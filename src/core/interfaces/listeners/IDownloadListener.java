package core.interfaces.listeners;

import data.Item;

/**
 * Interface to represent a Downloader Listener.
 * An instance of an object implementing this interface should be used to 
 * listen for the download completion.
 * @author Mario
 */
public interface IDownloadListener {
    /**
     * Override this function to define the operation that have to be executed
     * after the download has completed.
     * @param item The downloaded item.
     */
    public void downloadCompleted(Item item);
}
